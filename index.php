        <section class="page-section" id="sejarah">
            <div class="container">
                <div>
                    <h2 class="section-heading text-uppercase">Sejarah Universitas Jendral Achmad Yani</h2>
                </div>
                <div>
                    <p>Berdasar keinginan luhur keluarga besar TNI AD yang senantiasa eksis dalam perjuangan Bangsa Indonesia dalam mengisi kemerdekaan Republik Indonesia dan dilandasi oleh kesadaran yang tinggi bahwa keberhasilan perjuangan selama ini adalah berkat dukungan kerjasama dari rakyat Indonesia dan seluruh warga TNI AD.</p>
                    <p>Keluarga besar TNI AD melalui Yayasan Kartika Eka Paksi (YKEP) bertekad berperan aktif dalam melanjutkan pengabdiannya dalam semua aspek kehidupan sosial bangsa Indonesia yang bersendikan Pancasila. Peran aktif YKEP pada sektor pendidikan formal dilakukan melalui pendirian perguruan tinggi.</p>
                    <p>Akademi Teknik Jenderal Achmad Yani (ATA) didirikan pada tahun 1974. ATA berdiri berkat kerjasama antara Pimpinan TNI Angkatan Darat, Pertamina dan Institut Teknologi Bandung (ITB). Pada saat itu ATA memiliki 2 jurusan yakni Teknik Mesin dan Teknik Elektro. Baru pada tahun 1975 berdiri jurusan Teknik Kimia. Mahasiswa ATA tahun 1974 dan 1975 dikhususkan untuk putra dan putri TNI Angkatan Darat dengan segala pembiayaan ditanggung oleh TNI AD dan Pertamina sedangkan proses belajar mengajar dibina oleh ITB.</p>
                    <p>Dalam perkembangannya, ATA pada tahun 1976, 1977, 1978, tidak menerima mahasiswa. Baru pada tahun 1979 ATA menerima mahasiswa, akan tetapi selain putra putri TNI AD ATA menerima juga dari masyarakat umum. Biaya kuliah angkatan 1979 dan 1980 tidak ditanggung oleh TNI AD dan Pertamina lagi. Kuliah mahasiswa ATA dari 1974, 1975,1979 dan 1980 bertempat di PUSDIKJAS Jl. Pasirkumeli Cimahi dan SD Widiawan Jl. Taman Kartini Cimahi, sampai tahun 1984.</p>
                    <p>Tahun 1987 Akademi Teknologi Jenderal Achmad Yani dikembangkan menjadi Sekolah Tinggi Teknologi Jenderal Achmad Yani (STTA) berdasarkan Surat Keputusan Menteri Pendidikan dan Kebudayaan Republik Indonesia no 0451/0/1987, tanggal 1 Agustus 1987 tentang Perubahan Bentuk dan Nama Akademi Teknologi Jenderal Achmad Yani Cimahi menjadi Sekolah Tinggi Teknologi Jenderal Achmad Yani (STTA).</p>
                    <p>Tahun 1987 berdiri Sekolah Tinggi Ilmu Ekonomi Achmad Yani (STIEA) dan Sekolah Tinggi Matematika dan Ilmu Pengetahuan Alam Jenderal Achmad Yani (ST MIPA) berdasarkan Surat Keputusan KASAD selaku Ketua Umum YKEP nomor SKEP/047/II/1987 tanggal 12 Februari 1987 tentang Pendirian Sekolah Tinggi Ilmu Ekonomi Achmad Yani (STIEA) dan Sekolah Tinggi Matematika dan Ilmu Pengetahuan Alam Jenderal Achmad Yani (ST MIPA). Akademi Industri Logam (AIL) berdiri pada tahun 1972 merupakan lembaga pendidikan tingkat akademi di bawah naungan Yayasan Ikatan Keluarga Besar (IGASAR) PINDAD. AIL mempunyai Jurusan Teknik Industri dan Jurusan Metalurgi.</p>
                    <p>Pada tahun 1985 Akademi Industri Logam (AIL) dikembangkan menjadi Sekolah Tinggi Teknik Industri Bandung (STTIB) dengan 2 (dua) Jurusan, yaitu Jurusan Teknik Manajemen Industri (TMI) dan Jurusan Metalurgi. Pada tahun 1987 berdiri Jurusan Teknik Mesin yang menyelenggarakan Strata-1 (S-1). Pada tahun 1987 pengelolaan STTIB diserahkan dari Yayasan IGASAR PINDAD ke Yayasan Kartika Eka Paksi (YKEP).</p>
                    <p>Universitas Jenderal Achmad Yani (UNJANI) merupakan hasil penggabungan sekolah-sekolah tinggi yang dikelola oleh YKEP yaitu : Sekolah Tinggi Teknologi Jenderal Achmad Yani (STTA), Sekolah Tinggi Ilmu Ekonomi Jenderal Achmad Yani (STIEA), Sekolah Tinggi Matematika dan Ilmu Pengetahuan Alam (ST MIPA).</p>
                        <p> UNJANI berdiri pada tanggal 20 Mei 1990 yang ditetapkan dengan Surat Keputusan Ketua Umum YKEP nomor : 027/ YKEP/1990 tanggal 20 Mei 1990 yang selanjutnya dikukuhkan oleh MENDIKBUD dengan Surat Keputusan nomor : 0512/0/1990 tanggal 9 Agustus 1990. Nama Universitas Jenderal Achmad Yani yang disingkat UNJANI diambil dari salah satu tokoh Prajurit TNI AD, Pahlawan Revolusi Jenderal Achmad Yani.
                            <br>
                            Pada saat berdiri, UNJANI terdiri dari 3 Fakultas dan 8 Jurusan yaitu: <br>1. Fakultas Teknik ( Teknik Mesin, Teknik Elektro, Teknik Kimia dan Teknik Sipil)<br> 2. Fakultas MIPA ( Kimia, Fisika)<br>3. Fakultas Ekonomi (Manajemen dan Akutansi).</p>
                    <p>Pada tahun 1992 STTIB berintegrasi dengan UNJANI sesuai Surat Keputusan Menteri Pendidikan dan Kebudayaan Republik Indonesia Nomor : 0124/0/1992, tanggal 2 Maret 1992 di bawah binaan Fakultas Teknik. Dalam rangka meningkatkan peranan di dunia pendidikan, pada tahun 1992 UNJANI membuka Fakultas Kedokteran dengan Prodi Kedokteran Umum dengan Surat Menteri Pendidikan dan Kebudayaan RI Nomor 45/D/0/1993 tanggal 20 April 1993.</p>
                    <p>Pada tahun 1992 berdiri Fakultas Ilmu Sosial dan Ilmu Politik dengan Jurusan Hubungan Internasional dan Jurusan Ilmu Pemerintahan dengan Surat Keputusan Direktur Jenderal Pendidikan Tinggi Departemen Pendidikan dan Kebudayaan Republik Indonesia Nomor : 210/DIKTI/Kep/1992 tanggal 1 Juni 1992.</p>
                    <p>Tahun 1998 Fakultas Sosial dan Ilmu Politik membuka program S-2 Program Studi Magister Ilmu Pemerintahan dengan SK. Dirjen Dikti Depdibud Nomor : 440/DIKTI /Kep/1998.</p>
                    <p>Pada tahun 1999 Fakultas MIPA menambah Jurusan Farmasi dengan Surat Keputusan Direktorat Jenderal Pendidikan Tinggi Nomor : 17/Dikti/Kep/1999 tanggal 10 Mei 1999.</p>
                    <p>Tahun 2003 UNJANI menambah Fakultas Psikologi, sesuai dengan Surat Keputusan Rektor UNJANI Nomor: Skep/05/UNJANI/V/2003 tanggal 20 Mei 2003. Kemudian itu dikukuhkan berdasarkan Direktur Jenderal Perguruan Tinggi Direktorat Jenderal Pendidikan Tinggi Nomor: 190/D/T/2003 tanggal 30 Januari 2004.</p>
                    <p>Pada tahun 2005 Fakultas MIPA menambah Program Studi Ilmu Komputer dengan Surat Keputusan Direktorat Jenderal Pendidikan Tinggi Nomor : 3167/D/T/2005 tanggal 28 September 2005.</p>
